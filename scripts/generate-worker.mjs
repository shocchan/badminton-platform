// 本番ドメイン以外（staging / ブランチプレビュー等）は noindex を出す
function isProductionHost(hostname) {
  return hostname === 'kawabado.com' || hostname === 'www.kawabado.com';
}

// HTML レスポンスに noindex ヘッダを付けるべきホストか
function shouldNoindex(hostname) {
  return !isProductionHost(hostname);
}

function htmlResponseHeaders(hostname, extra) {
  const headers = {
    'Content-Type': 'text/html; charset=utf-8',
    'Cache-Control': 'no-cache, must-revalidate',
  };
  if (shouldNoindex(hostname)) {
    headers['X-Robots-Tag'] = 'noindex, nofollow';
  }
  if (extra) Object.assign(headers, extra);
  return headers;
}

