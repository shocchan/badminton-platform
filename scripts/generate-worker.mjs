async function checkIdExists(env, kind, id) {
  const supabaseUrl = env.VITE_SUPABASE_URL || env.SUPABASE_URL || '';
  const supabaseKey = env.VITE_SUPABASE_ANON_KEY || env.SUPABASE_ANON_KEY || '';
  // 環境変数無しは fail-open（開発時など）
  if (!supabaseUrl || !supabaseKey) return { exists: true, dbError: true };

  let query = '';
  if (kind === 'tournament') {
    query = '/rest/v1/tournaments?id=eq.' + encodeURIComponent(id) + '&visibility=neq.draft&select=id&limit=1';
  } else if (kind === 'blog') {
    query = '/rest/v1/blog_posts?id=eq.' + encodeURIComponent(id) + '&select=id&limit=1';
  } else if (kind === 'activity') {
    query = '/rest/v1/activities?id=eq.' + encodeURIComponent(id) + '&select=id&limit=1';
  } else {
    return { exists: true, dbError: true };
  }

  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), 2500);
  try {
    const res = await fetch(supabaseUrl + query, {
      headers: { apikey: supabaseKey, Authorization: 'Bearer ' + supabaseKey },
      signal: controller.signal,
    });
    clearTimeout(timer);
    if (!res.ok) return { exists: true, dbError: true }; // 5xx等はfail-open
    const rows = await res.json();
    if (!Array.isArray(rows)) return { exists: true, dbError: true };
    return { exists: rows.length > 0, dbError: false };
  } catch (_) {
    clearTimeout(timer);
    return { exists: true, dbError: true };
  }
}

// pathname から DB 存在チェックが必要な種別と ID を抽出。無ければ null。
