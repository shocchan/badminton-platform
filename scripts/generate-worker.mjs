/**
 * ビルド後にdist/index.htmlを読み取り、
 * 正しいハッシュを埋め込んだdist/_worker.jsを自動生成するスクリプト。
 * CF Pages v3 の Clean URLs バグを回避するため index.html を Worker に埋め込む。
 */
import { readFileSync, writeFileSync } from 'fs';

const indexHtml = readFileSync('dist/index.html', 'utf8');
// バッククオートをエスケープ
const escapedHtml = indexHtml.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

const workerContent = `// Auto-generated by scripts/generate-worker.mjs
// CF Pages v3 Clean URLs 500エラーを回避するためindex.htmlを直接埋め込む

const INDEX_HTML = \`${escapedHtml}\`;

export default {
  async fetch(request, env) {
    const url = new URL(request.url);
    const pathname = url.pathname;

    // /__debug エンドポイント（開発用）
    if (pathname === '/__debug') {
      const results = {};
      for (const p of ['/favicon.svg', '/assets/index-CoAYpyrw.css']) {
        try {
          const r = await env.ASSETS.fetch(new Request(url.origin + p));
          results[p] = { status: r.status };
        } catch (e) {
          results[p] = { error: e.message };
        }
      }
      return new Response(JSON.stringify(results, null, 2), {
        headers: { 'Content-Type': 'application/json' },
      });
    }

    // 拡張子があるファイル（JS, CSS, 画像など）はenv.ASSETSで直接配信
    const hasExtension = /\\.[a-zA-Z0-9]+$/.test(pathname);
    if (hasExtension && !pathname.endsWith('.html')) {
      try {
        const assetResponse = await env.ASSETS.fetch(request);
        if (assetResponse.status < 400) return assetResponse;
      } catch (_) {}
    }

    // HTMLルートはWorkerに埋め込まれたindex.htmlを返す
    return new Response(INDEX_HTML, {
      status: 200,
      headers: { 'Content-Type': 'text/html; charset=utf-8' },
    });
  },
};
`;

writeFileSync('dist/_worker.js', workerContent);
console.log('✅ Generated dist/_worker.js with embedded index.html');
